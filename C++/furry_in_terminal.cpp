#include <iostream>
#include <string>
#include <chrono>
#include <thread>
using namespace std;

void printBoykisser()
{
    string art[] = {
        "@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@+@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@",
        "@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@#: +@@@@@@@@@@%*=----=%@@@@@@@@@@@@@@@",
        "@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@+=@%-   -@@@@@@@@@+        .=#%@@@@@@@@@@@@",
        "@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@: ..    -@@@@@@@@-     -*##*=-#@@@@@@@@@@@@",
        "@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@%%%%%%%%%%%%%%%%%%%%%:        .=*%@@%*     .*%@@@@%@@@@@@@@@@@@@",
        "@@@@@@@@@@@@@@@%%%%%%%%%%%%%%%%%%%%%%%%%%:=%%%%%%%%%%%%%=         .=-%%%%=      %%%%%%%%@@@@@@@@@@@@",
        "%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%*  +###########=..==          -#%=      :#%%%%%%%%%%%%%%%%%%",
        "%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%######=   *####*******==**+::-*-      *#.       -+#%%%%%%%%%%%%%%%",
        "%%%%%%%%%%%%%%%%%#######################*.  .*********++++**+-::==      -#*.        *%%%%%%%%%%%%%%%",
        "%%##################################*****+   :+++++++++++++: .:         .##*.       .*#######%%%%%%%",
        "###################################*******=   -+++++++++++:.:+=-::.      *#*#-       *######%%%%%%%%",
        "#######*+*#############################****:   =****+++++*=..:-=+**      =###+      .*######%%%%%%%%",
        "%%%%*+*###++*#############*+++*###########-..   +********-       .-      .###:     .*#####%%%%%%%%%%",
        "%%#+#%%%%%%%*+#%%%%%%%%%*+#%%##++*#######- +#-  .*######=                .*=.     :#%%%%%%%%%%%%%%%%",
        "#+*%%%%%%%%%%%*+#%%%%%#+#%%%%%%%%*++#%%#+ :#*#*-.*#######=.                   .:-+%%%%%%%%%%%%%%%%%%",
        "#%%%%%%%%%%%%%%%*******%%%%%%%%%%%%%****=:::...:-#%#########+-         :*###%%%%##%%%%%%%%%%%%%%%%%%",
        "%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%@%%%%%%%%%%%%%%%%%%%%%%%##*%%##%@@%%%%%%%%%%%%%%%%%%%%%%%%%%%"};

    for (const string &line : art)
    {
        for (char c : line)
        {
            // Цвет зелёный, если поддерживаются ANSI escape-коды
            cout << "\033[1;32m" << c << "\033[0m";
            cout.flush();
            this_thread::sleep_for(chrono::milliseconds(5)); // Задержка для каждого символа
        }
        cout << endl;
        this_thread::sleep_for(chrono::milliseconds(50)); // Задержка для каждой строки
    }
}

int main()
{
    printBoykisser();
    return 0;
}
